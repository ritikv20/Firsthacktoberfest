test_case = input()

test_case = int(test_case)
    
for i in range (0, test_case):
        
        num = input()
        
        num = int(num)
        
        bnr = bin(num).replace('0b','')
        
        x = bnr[::-1]
        
        while len(x) < 8:
         x += '0'
        
        bnr = x[::-1]
        
        print(bnr)
